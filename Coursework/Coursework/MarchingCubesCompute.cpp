void MarchingCubesCompute::SendData(ID3D11DeviceContext* deviceContext, D3D_PRIMITIVE_TOPOLOGY top)
{
    unsigned int stride;
    unsigned int offset;
	
    // Set vertex buffer stride and offset.
    stride = sizeof(VertexType);
    offset = 0;

    deviceContext->IASetVertexBuffers(0, 1, &VertexBuffer, &stride, &offset);
    deviceContext->IASetPrimitiveTopology(top);
}

void MarchingCubesCompute::unbind(ID3D11DeviceContext* dc)
{
    dc->CopyStructureCount(CounterBuffer, 0, VertUAV);

    D3D11_MAPPED_SUBRESOURCE mappedResource;

    dc->Map(CounterBuffer, 0, D3D11_MAP_READ, 0, &mappedResource);
    AppendCount = (*(UINT*)mappedResource.pData);
    dc->Unmap(CounterBuffer, 0);

    dc->CopyResource(VertexBuffer, AppendVertexBuffer);

    // Unbind output from compute shader
    ID3D11UnorderedAccessView * nullUAV[] = { NULL };
    dc->CSSetUnorderedAccessViews(0, 1, nullUAV, 0);

    ID3D11ShaderResourceView* nullSrv[] = {NULL};
    dc->CSSetShaderResources(0,1,nullSrv);
    dc->CSSetShaderResources(1,1,nullSrv);
    // Disable Compute Shader
    dc->CSSetShader(nullptr, nullptr, 0);
}



